//     TABLE UPDATE
     private void table_update(){
         int c;
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/DemoInfo","root","");
            insert = con1.prepareStatement("select * from Info");
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            
            c = rss.getColumnCount();
            DefaultTableModel df = (DefaultTableModel) Student_table.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                
                Vector v2 = new Vector();
                
                for(int a=1; a<=c;a++){
                    
                    v2.add(rs.getString("Id"));
                    v2.add(rs.getString("Name"));
                    v2.add(rs.getString("Age"));
                    v2.add(rs.getString("Phno"));
                    
                }
                df.addRow(v2);
                
            }
        
        } 
        catch (Exception ex){
            
        }
     }
     
    
