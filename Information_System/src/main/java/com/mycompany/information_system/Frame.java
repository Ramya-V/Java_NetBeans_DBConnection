    private void Insert_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Insert_buttonActionPerformed
   
        String name = Name_text.getText();
        String age = Age_text.getText();
        String phno = Phno_text.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/DemoInfo","root","");
            insert = con1.prepareStatement("insert into Info(Name, Age, Phno)values(?,?,?)");
            insert.setString(1,name);
            insert.setString(2,age);
            insert.setString(3,phno);
            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Inserted Successfully");
            table_update();
            
            Name_text.setText("");
            Age_text.setText("");
            Phno_text.setText("");
            Name_text.requestFocus();

        } 
        catch (ClassNotFoundException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (SQLException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Insert_buttonActionPerformed

