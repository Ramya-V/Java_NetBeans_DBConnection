//    UPDATE OPERATION
    private void Update_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_buttonActionPerformed
        
        DefaultTableModel df = (DefaultTableModel) Student_table.getModel();       
        int selectedIndex = Student_table.getSelectedRow();
        
        try {
            int id = Integer.parseInt(df.getValueAt(selectedIndex, 0).toString());
            String name = Name_text.getText();
            String age = Age_text.getText();
            String phno = Phno_text.getText();
            
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/DemoInfo","root","");
            insert = con1.prepareStatement("update Info set Name=?, Age=?, Phno=? where Id=?");
            insert.setString(1,name);
            insert.setString(2,age);
            insert.setString(3,phno);
            insert.setInt(4,id);
            insert.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null,"Updated Successfully");
            table_update();
           
            Name_text.setText("");
            Age_text.setText("");
            Phno_text.setText("");
            Name_text.requestFocus();
         
        } 
        catch (ClassNotFoundException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (SQLException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Update_buttonActionPerformed

